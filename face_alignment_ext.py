def CropFace(image, eye_left, eye_right, offset_pct=(0.2,0.2), dest_sz = (100,100)):
  # calculate offsets in original image
  offset_h = math.floor(float(offset_pct[0])*dest_sz[0])
  offset_v = math.floor(float(offset_pct[1])*dest_sz[1])
  # get the direction
  eye_direction = (eye_right[0] - eye_left[0], eye_right[1] - eye_left[1])
  # calc rotation angle in radians
  rotation = -math.atan2(float(eye_direction[1]),float(eye_direction[0]))
  # distance between them
  dist = Distance(eye_left, eye_right)
  # calculate the reference eye-width
  reference = dest_sz[0] - 2.0*offset_h
  # scale factor
  scale = float(dist)/float(reference)
  # rotate original around the left eye
  image = ScaleRotateTranslate(image, center=eye_left, angle=rotation)
  # crop the rotated image
  crop_rect = (eye_left[0] - scale*offset_h, eye_left[1] - scale*offset_v,
               dest_sz[0]*scale, dest_sz[1]*scale)
  image = crop_image(image, crop_rect)
  # resize it
  image = cv2.resize(image, dest_sz)
  return image


def crop_image(img, rect):
    x, y, w, h = rect
    return img[y:y+h, x:x+w]